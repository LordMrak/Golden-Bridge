         <h3>Описание</h3>
<p>Робот разработан для извлечения прибыли на новостных движениях. Волатильность в момент выхода новостей особой важности бывает очень значительной. Все трейдеры пытаются заработать в период выхода сильных новостей, но многие испытывают стресс и просто физически не успевают делать простые и понятные вещи, которые ведут к стабильному росту депозита.</p> 
<p>Идея заключалась в том, что робот способен устанавливать приказы очень быстро и точно. Для его работы нужно только быстрое исполнение ордеров, что гарантирую большое количество брокеров в своих счетах ESN и NDD.</p>
<p>Данному роботу устанавливается дата и время выхода новости. Далее робот анализирует количество секунд до новости и когда видит заданное значение он выставляет отложенные ордера, но не от текущей цены, а от минимально разрешенной дистанции для отложенных ордеров. Выставляются t/p и s/l. Робот переводит в безубыток стоп приказ, по прохождении заданного количества пунктов в прибыльной зоне.  Можно активировать трал, что очень актуально для новостей.
</p>


